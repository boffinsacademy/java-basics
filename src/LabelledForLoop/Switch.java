package LabelledForLoop;

public class Switch {

	public static void main(String[] args) {
		int num =10;
		switch(num){
		
		case 1: {
			System.out.println("one");
			break;
		}
		case 10: {
			System.out.println("ten");
			break;
		}
		case 20: {
			System.out.println("twenty");
			break;
		}
		
		 default: {
			System.out.println("default printing");
		}
		
		
		}

	}

}
